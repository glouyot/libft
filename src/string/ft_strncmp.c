#include "libft.h"

int		ft_strncmp(char const *s1, char const *s2, size_t n)
{
	while ((s1 || s2) && n--)
	{
		if (*s1 != *s2)
			return (*(t_byte *)s1 - *(t_byte *)s2);
		else if (*s1 == '\0')
			return (0);
		s1++;
		s2++;
	}
	return (0);
}
