#include "libft.h"

int		ft_memcmp(void const *s1, void const *s2, size_t n)
{
	while (n--)
	{
		if (*(t_byte *)(s1) != *(t_byte *)(s2))
			return (*(t_byte *)(s1) - *(t_byte *)(s2));
		s1++;
		s2++;
	}
	return (0);
}
