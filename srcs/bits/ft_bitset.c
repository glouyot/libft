#include "libft.h"

void		ft_bitset(void *bits, t_byte n, t_byte x)
{
	if (x)
		((char *)bits)[n / 8] |= 1 << (n % 8);
	else
		((char *)bits)[n / 8] &= ~(1 << (n % 8));
}
