#include "libft.h"

int			ft_prschrpos(char const *str, char c)
{
	int		i;

	if (c == '\0')
		return (-1);
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
