#include "libft.h"

int		ft_strcchr(char const *s, int c)
{
	int	i;
	int	ret;

	i = 0;
	ret = 0;
	while (s[i])
	{
		if (s[i] == c)
			ret++;
		i++;
	}
	return (ret);
}
